################### Numeric Type ################
x = 10  # int
y = 3.14  # float
z = 2 + 3j  # complex

################### Text Type ################
name = "John Doe"  # str
message = "Hello, World!"  # str


################### Boolean Type ################
is_valid = True  # bool
is_admin = False  # bool

################### Sequence Types ################
my_list = [1, 2, 3, 4, 5]  # list
my_tuple = (10, 20, 30)  # tuple
my_range = range(5)  # range

################### Mapping Type ################
person = {"name": "Alice", "age": 25, "city": "London"}  # dict


################### Set Types ################
my_set = {1, 2, 3, 4, 5}  # set
my_frozenset = frozenset({10, 20, 30})  # frozenset


################### Other Types ################
my_bytes = b"Hello"  # bytes
my_bytearray = bytearray(b"World")  # bytearray
my_memoryview = memoryview(b"Python")  # memoryview
my_none = None  # NoneType
